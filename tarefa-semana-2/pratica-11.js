let cores = function (nomeCor) {
    switch (nomeCor) {
        case "Vermelha":
            console.log('rgb(255,0,0)');
            break;
        case "Laranja":
            console.log('rgb(255,128,0)');
            break;
        case "Amarela":
            console.log('rgb(255,255,0)');
            break;
        case "Verde":
            console.log('rgb(,255,)')
            break;
        case "Azul claro":
            console.log('rgb(0,255,255)');
            break;
        case "Azul marinho":
            console.log('rgb(0,0,255)');
            break;
        case "Rosa":
            console.log('rgb(255,0,255)')
    }
}

cores("Vermelha")
cores("Laranja")